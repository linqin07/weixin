package com.entity;


public class JsonFormat {


    /**
     * 使用jsonformat插件快速建立实体类。快捷键alt+s 或者 alt+insert
     *
     * name : 菜鸟教程
     * url : www.runoob.com
     */

    private String name;
    private String url;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }


}
